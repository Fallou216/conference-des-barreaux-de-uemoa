                    <div class="col-lg-6 order-lg-1">
                        <div class="details-content">
                            <h2 class="section-title">Activités Récentes de la Conférence des Barreaux de l'UEMOA</h2>
                            <p>
                                En septembre 2024, la Conférence des Barreaux de l'UEMOA a organisé le 3ème Congrès des
                                Avocats à Abidjan, Côte d'Ivoire. Cet événement a rassemblé 361 participants, y compris
                                des représentants de pays observateurs tels que la Guinée et le Cameroun.
                            </p>
                            <p>
                                Le congrès, placé sous le thème « Dix ans d’application du Règlement n°05 : retour vers
                                le futur de l’avocature de l’espace UEMOA », a permis d'évaluer l'impact du Règlement
                                n°05 sur la profession d'avocat et de formuler cinq recommandations majeures pour
                                relever les défis actuels de la profession dans l'espace communautaire.
                            </p>
                            <p>
                                Ces recommandations visent à renforcer l'harmonisation des pratiques juridiques,
                                améliorer la formation continue des avocats et promouvoir une meilleure coordination
                                entre les barreaux des États membres.
                            </p>
