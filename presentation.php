                        <div class="details-content">
                            <h2 class="section-title">Les Organes de l'UEMOA</h2>
                            <p>
                                L'UEMOA est structurée autour de plusieurs organes clés :
                            </p>
                            <ul>
                                <li><strong>La Conférence des Chefs d'État et de Gouvernement</strong> : Organe suprême
                                    de l'Union, elle définit les grandes orientations politiques.</li>
                                <li><strong>Le Conseil des Ministres</strong> : Composé des ministres des finances et de
                                    l'économie des États membres, il adopte les règlements et directives.</li>
                                <li><strong>La Commission</strong> : Organe exécutif chargé de la mise en œuvre des
                                    politiques de l'Union.</li>
                                <li><strong>La Cour de Justice</strong> : Assure le respect du droit communautaire.</li>
                                <li><strong>La Cour des Comptes</strong> : Contrôle les finances de l'Union.</li>
                                <li><strong>Le Comité Interparlementaire</strong> : Organe de contrôle démocratique
                                    représentant les parlements nationaux.</li>
                                <li><strong>La Chambre Consulaire Régionale</strong> : Représente les opérateurs
                                    économiques et assure le dialogue entre le secteur privé et les instances de
                                    l'Union.</li>
                            </ul>
                            <p>
                                Ces organes collaborent pour assurer l'intégration économique et monétaire des États
                                membres de l'UEMOA.
                            </p>
                        </div>
                    </div>
                </div>
